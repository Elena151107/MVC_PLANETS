"""
Создайте консольное приложение, которое моделирует планетарную систему.
Пользователь может добавлять планеты, задавать их характеристики, просматривать список планет с их параметрами,
запускать симуляцию орбитального движения (упрощённо), а также удалять планеты из системы.
Особенности:
- Model: Хранит данные о планетах (имя, радиус орбиты, скорость вращения вокруг звезды, массу).
- View: отображает список планет, параметры отдельной планеты, анимацию движения (в виде текстового
описания, например, "Планета X завершила 1 полный оборот").
- Controller: организует логику управления: добавление, удаление, просмотр, запуск симуляции.

Пример работы:
1. Показать планеты
2. Добавить планету
3. Удалить планету
4. Запустить симуляцию
5. Выйти

Выберите действие: 2
Введите имя планеты: Земля
Введите радиус орбиты: 149.6
Введите скорость вращения: 0.1
Введите массу планеты: 5.972

Выберите действие: 4
Введите количество шагов симуляции: 3
Введите временной шаг: 10
Шаг 1: Планета Земля на позиции x=149.59, y=0.26.
Шаг 2: Планета Земля на позиции x=149.58, y=0.52.
Шаг 3: Планета Земля на позиции x=149.55, y=0.78.
"""

class Planet:
    def __init__(self, name):
        self.name = name
        self.radius = None
        self.speed = None
        self.mass = None

    def set_radius(self, radius):
        self.radius = radius
        return float(self.radius)

    def set_speed(self, speed):
        self.speed = speed
        return float(self.speed)

    def set_mass(self, mass):
        self.mass = mass
        return float(self.mass)


